package org.pi4j.model;

public class PeerIndex {

	private String version;
	private String peerindex_id;
	private int peerindex;
	
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getPeerindex_id() {
		return peerindex_id;
	}
	public void setPeerindex_id(String peerindex_id) {
		this.peerindex_id = peerindex_id;
	}
	public int getPeerindex() {
		return peerindex;
	}
	public void setPeerindex(int peerindex) {
		this.peerindex = peerindex;
	}
	
}
